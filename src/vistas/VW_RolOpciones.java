package vistas;

public class VW_RolOpciones {

	//Atributos
	private int ID;
	private String Rol;
	private String Opcion;
	
	//Metodos
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getRol() {
		return Rol;
	}
	public void setRol(String rol) {
		Rol = rol;
	}
	public String getOpcion() {
		return Opcion;
	}
	public void setOpcion(String opcion) {
		Opcion = opcion;
	}
	
	
}
